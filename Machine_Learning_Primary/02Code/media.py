
class Movie():
    """
    Initialize the Movie class, and data points
    title/storyline/poster_image/trailer
    should be remember.
    """
    def __init__(self, movie_title, movie_storyline,
                 poster_image, trailer_youtube):
        """
        define the function init, initialize pieces of information like title,
        story line and others that we want
        to remember inside our class.
        """
        self.title = movie_title
        self.storyline = movie_storyline
        self.poster_image_url = poster_image
        self.trailer_youtube_url = trailer_youtube
        





















